function Mostrar()
{
    var mes=prompt("Ingrese un mes del año");
    switch(mes){
        case "Enero":
                    alert("Comienza el año");
                    break;
        case "Diciembre":
                    alert("Se vienen las fiestas")
                    break;
        default:
                alert("No es Diciembre ni enero")                        
    }

}
